type LanguageCode = keyof typeof translations;
type TranslationKey = keyof typeof translations['en-IN'];

export const useTranslation = (lang: LanguageCode | null) => {
  const t = (key: TranslationKey, replacements?: { [key: string]: string | number }): string => {
    const languageToUse = lang && translations[lang] ? lang : 'en-IN';
    let text = (translations[languageToUse] as any)[key] || translations['en-IN'][key] || key;
    
    if (replacements) {
      Object.keys(replacements).forEach(placeholder => {
        text = text.replace(`{${placeholder}}`, String(replacements[placeholder]));
      });
    }
    
    return text;
  };

  return { t };
};
